using Xunit;
using Shouldly;

public class CalculatorTests
{
    public class Add
    {
        [Theory]
        [InlineData(1, 2, 3)]
        [InlineData(-1, -2, -3)]
        [InlineData(0, 0, 0)]
        [InlineData(10, -5, 5)]
        [InlineData(-10, 10, 0)]
        public void Add_正しい引数を渡した場合_正しい結果を返す(int a, int b, int expected)
        {
            ////////////////////////////////////////////////////////////////////////
            // Arrange
            ////////////////////////////////////////////////////////////////////////
            var calculator = new Calculator();

            ////////////////////////////////////////////////////////////////////////
            // Act
            ////////////////////////////////////////////////////////////////////////
            var result = calculator.Add(a, b);

            ////////////////////////////////////////////////////////////////////////
            // Assert
            ////////////////////////////////////////////////////////////////////////
            result.ShouldBe(expected);
        }
    }
}